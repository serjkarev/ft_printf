char	*ft_strcpy(char *dst, const char *src)
{
	int		i;
	char	*ds;
	char	*sr;

	i = 0;
	ds = (char*)dst;
	sr = (char*)src;
	while (sr[i] != '\0')
	{
		ds[i] = sr[i];
		i++;
	}
	ds[i++] = '\0';
	return (dst);
}
